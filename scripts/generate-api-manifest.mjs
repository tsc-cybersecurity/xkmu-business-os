#!/usr/bin/env node
/**
 * Build-Time API-Manifest-Generator
 * ===================================
 * Scannt src/app/api recursively, extrahiert pro route.ts:
 *   - URL-Pfad (mit [id] -> {id})
 *   - exportierte HTTP-Methoden (GET/POST/PUT/PATCH/DELETE)
 *   - withPermission(req, 'module', 'action', ...) -> Auth-Info
 *
 * Schreibt src/lib/api-docs/discovered.ts als getypte Module.
 *
 * Laeuft automatisch via `prebuild` und kann manuell mit
 * `npm run api-docs:scan` ausgeloest werden.
 */

import { readFileSync, writeFileSync, readdirSync, statSync, mkdirSync } from 'node:fs'
import { join, relative, sep } from 'node:path'

const ROOT = process.cwd()
const API_ROOT = join(ROOT, 'src', 'app', 'api')
const OUT_PATH = join(ROOT, 'src', 'lib', 'api-docs', 'discovered.ts')
const SOURCES_PATH = join(ROOT, 'src', 'lib', 'api-docs', 'discovered-sources.server.ts')

const HTTP_METHODS = ['GET', 'POST', 'PUT', 'PATCH', 'DELETE']

function walk(dir) {
  const out = []
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry)
    const st = statSync(full)
    if (st.isDirectory()) {
      out.push(...walk(full))
    } else if (entry === 'route.ts' || entry === 'route.tsx') {
      out.push(full)
    }
  }
  return out
}

function pathFromFile(file) {
  const rel = relative(join(ROOT, 'src', 'app'), file).split(sep).join('/')
  const stripped = rel.replace(/\/route\.tsx?$/, '')
  const normalized = stripped.replace(/\[([^\]]+)\]/g, '{$1}')
  return '/' + normalized
}

function extractMethods(src) {
  const found = new Set()
  for (const m of HTTP_METHODS) {
    const re = new RegExp(`export\\s+(?:async\\s+)?(?:function|const)\\s+${m}\\b`)
    if (re.test(src)) found.add(m)
  }
  return [...found]
}

function extractAuth(src, method) {
  const startRe = new RegExp(`export\\s+(?:async\\s+)?(?:function|const)\\s+${method}\\b`)
  const startMatch = startRe.exec(src)
  if (!startMatch) return null
  const sliceFrom = startMatch.index
  const nextExport = src.indexOf('\nexport ', sliceFrom + 1)
  const body = nextExport === -1 ? src.slice(sliceFrom) : src.slice(sliceFrom, nextExport)

  const permRe = /withPermission\s*\([^,]+,\s*['"`]([^'"`]+)['"`]\s*,\s*['"`]([^'"`]+)['"`]/
  const pm = permRe.exec(body)
  if (pm) return { type: 'session', module: pm[1], action: pm[2] }

  if (/getSession\s*\(|requireAuth\s*\(/.test(body)) return { type: 'session', module: null, action: null }
  if (/requireApiKey\s*\(/.test(body)) return { type: 'api-key', module: null, action: null }

  return { type: 'public', module: null, action: null }
}

function serviceSlugFromPath(p) {
  const cleaned = p.replace(/^\/api(\/v\d+)?\//, '')
  return cleaned.split('/')[0] || 'misc'
}

function writeManifest(endpoints, serviceCount) {
  // Slim-Manifest fuer Client + Server: method, path, auth, file (kein Source-Code).
  const slim = endpoints.map((e) => ({ method: e.method, path: e.path, auth: e.auth, file: e.file }))

  const out = `// AUTO-GENERATED by scripts/generate-api-manifest.mjs - DO NOT EDIT.
// Regenerate via \`npm run api-docs:scan\` (laeuft auch im prebuild-Hook).

import type { DiscoveredEndpoint } from './types'

export const DISCOVERED_AT = '${new Date().toISOString()}'
export const DISCOVERED_ENDPOINT_COUNT = ${endpoints.length}
export const DISCOVERED_SERVICE_COUNT = ${serviceCount}

export const DISCOVERED_ENDPOINTS: DiscoveredEndpoint[] = ${JSON.stringify(slim, null, 2)}
`
  mkdirSync(join(ROOT, 'src', 'lib', 'api-docs'), { recursive: true })
  writeFileSync(OUT_PATH, out, 'utf8')

  // Server-only Quelltext-Map fuer Annotation-Generator. ".server.ts"-Suffix
  // hindert Webpack daran, die Datei in Client-Bundles zu ziehen.
  const sources = endpoints.reduce((acc, e) => {
    acc[`${e.method} ${e.path}`] = e.sourceSnippet
    return acc
  }, {})

  const sourcesOut = `// AUTO-GENERATED by scripts/generate-api-manifest.mjs - DO NOT EDIT.
// SERVER-ONLY: enthaelt route.ts-Quelltext fuer Annotation-Generator.
// Niemals aus Client-Komponenten importieren — bricht das Client-Bundle auf.

export const DISCOVERED_SOURCES: Record<string, string> = ${JSON.stringify(sources, null, 2)}
`
  writeFileSync(SOURCES_PATH, sourcesOut, 'utf8')
}

function main() {
  if (!statSync(API_ROOT, { throwIfNoEntry: false })) {
    console.warn(`[api-manifest] ${API_ROOT} not found - writing empty stub (Docker deps-stage / fresh clone)`)
    writeManifest([], 0)
    return
  }

  const files = walk(API_ROOT)
  const endpoints = []

  for (const file of files) {
    const src = readFileSync(file, 'utf8')
    const methods = extractMethods(src)
    if (methods.length === 0) continue
    const p = pathFromFile(file)
    // Source-Snippet ins Manifest baken, damit Annotation-Generator in Prod
    // ohne FS-Zugriff auf src/app/api auskommt.
    const sourceSnippet = src.slice(0, 8000)
    for (const method of methods) {
      const auth = extractAuth(src, method)
      endpoints.push({
        method,
        path: p,
        auth,
        file: relative(ROOT, file).split(sep).join('/'),
        sourceSnippet,
      })
    }
  }

  endpoints.sort((a, b) =>
    a.path === b.path
      ? HTTP_METHODS.indexOf(a.method) - HTTP_METHODS.indexOf(b.method)
      : a.path.localeCompare(b.path),
  )

  const serviceCounts = endpoints.reduce((acc, e) => {
    const slug = serviceSlugFromPath(e.path)
    acc[slug] = (acc[slug] || 0) + 1
    return acc
  }, {})

  writeManifest(endpoints, Object.keys(serviceCounts).length)

  console.log(`[api-manifest] ${endpoints.length} Endpoints in ${Object.keys(serviceCounts).length} Services -> ${relative(ROOT, OUT_PATH)} + ${relative(ROOT, SOURCES_PATH)}`)
}

main()
