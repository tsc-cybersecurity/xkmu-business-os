// All SOPs combined
const SOPS: SopEntry[] = [...GENERAL_SOPS, ...OPERATIVE_SOPS]

export async function seedSopCatalog(_tenantId: string) {
  logger.info('Seeding SOP Catalog...', { module: MOD })

  // 1. Deliverable-Lookup-Map aufbauen
  const allDeliverables = await db
    .select({ id: deliverables.id, name: deliverables.name })
    .from(deliverables)
    

  const deliverableByName = new Map(allDeliverables.map(d => [d.name, d.id]))
  logger.info(`${allDeliverables.length} Deliverables geladen fuer Lookup`, { module: MOD })

  let created = 0
  let skipped = 0
  let warnCount = 0

  for (const sop of SOPS) {
    // 2. Idempotenz pruefen (zweistufig)
    if (sop.source_task_id) {
      const [existing] = await db
        .select({ id: sopDocuments.id })
        .from(sopDocuments)
        .where(
          and(eq(sopDocuments.sourceTaskId, sop.source_task_id),
            isNull(sopDocuments.deletedAt)))
      if (existing) {
        skipped++
        continue
      }
    } else {
      const [existing] = await db
        .select({ id: sopDocuments.id })
        .from(sopDocuments)
        .where(
          and(eq(sopDocuments.title, sop.title),
            isNull(sopDocuments.deletedAt)))
      if (existing) {
        skipped++
        continue
      }
    }

    // 3. Deliverable-Verknuepfung aufloesen
    let producesDeliverableId: string | null = null
    if (sop.produces_deliverable) {
      const delivName = DELIVERABLE_CODE_TO_NAME[sop.produces_deliverable] ?? null
      if (delivName) {
        producesDeliverableId = deliverableByName.get(delivName) ?? null
        if (!producesDeliverableId) {
          logger.warn(`Deliverable nicht gefunden: ${sop.produces_deliverable} -> ${delivName}`, { module: MOD })
          warnCount++
        }
      } else {
        logger.warn(`Kein Deliverable-Name fuer Code: ${sop.produces_deliverable} (SOP: ${sop.sop_id})`, { module: MOD })
        warnCount++
      }
    }

    // 4. SOP direkt einfuegen (db.insert, NICHT SopService.create)
    await db.insert(sopDocuments).values({
      title: sop.title,
      category: sop.category,
      status: sop.status ?? 'draft',
      version: '1.0.0',
      automationLevel: sop.automation_level ?? null,
      aiCapable: sop.automation_level !== 'manual',
      maturityLevel: sop.maturity_level ?? null,
      estimatedDurationMinutes: parseDauer(sop.dauer),
      subprocess: sop.subprocess ?? null,
      sourceTaskId: sop.source_task_id ?? null,
      producesDeliverableId,
      purpose: sop.output_beschreibung ?? null,
      scope: null,
      tools: [],
      tags: [],
    })
    created++
  }

  logger.info(
    `SOP Catalog seeded: ${created} erstellt, ${skipped} bereits vorhanden, ${warnCount} Deliverable-Lookup-Warnungen`,
    { module: MOD })
}
