// dauer -> estimatedDurationMinutes Parsing
function parseDauer(dauer: string | undefined): number | null {
  if (!dauer) return null
  // "30 Min" -> 30, "60 Min/Monat" -> 60, "5 Min/Tag" -> 5, "15 Min/Woche" -> 15
  const minMatch = dauer.match(/^(\d+)\s*Min/)
  if (minMatch) return parseInt(minMatch[1])
  // "4-8 Std" -> (4+8)/2 * 60 = 360, "2 Std/Jahr" -> 120, "1 Std" -> 60
  const stdMatch = dauer.match(/^(\d+)(?:-(\d+))?\s*Std/)
  if (stdMatch) {
    const low = parseInt(stdMatch[1])
    const high = stdMatch[2] ? parseInt(stdMatch[2]) : low
    return Math.round(((low + high) / 2) * 60)
  }
  // "5-10 Min" -> (5+10)/2 = 7 (rounded)
  const minRangeMatch = dauer.match(/^(\d+)-(\d+)\s*Min/)
  if (minRangeMatch) {
    const low = parseInt(minRangeMatch[1])
    const high = parseInt(minRangeMatch[2])
    return Math.round((low + high) / 2)
  }
  // "Einmalig", "Variabel" -> null
  return null
}

